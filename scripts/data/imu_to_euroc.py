#!/usr/bin/env python3
"""
Convert a per-sequence imu.csv into EuRoC-ASL format mav0/imu0/data.csv.

EuRoC layout expected by AirSLAM / Basalt / ORB-SLAM3 stereo-inertial:
  mav0/imu0/data.csv      header: #timestamp [ns],w_RS_S_x,w_RS_S_y,w_RS_S_z,
                                  a_RS_S_x,a_RS_S_y,a_RS_S_z
  mav0/imu0/sensor.yaml   sensor metadata (rate, noise, T_BS)

Source imu.csv format (Rosario v2, written by scripts/data/_rosario_extract.py):
  t,ax,ay,az,gx,gy,gz       seconds (float), accel m/s^2, gyro rad/s

Usage:
    python3 scripts/data/imu_to_euroc.py <seq_dir> [--rate 200]

Outputs <seq_dir>/mav0/imu0/data.csv and a minimal sensor.yaml.
"""
import argparse
import csv
from pathlib import Path


HEADER = (
    "#timestamp [ns],w_RS_S_x [rad s^-1],w_RS_S_y [rad s^-1],"
    "w_RS_S_z [rad s^-1],a_RS_S_x [m s^-2],a_RS_S_y [m s^-2],a_RS_S_z [m s^-2]"
)


def convert(seq_dir: Path, rate_hz: int) -> int:
    src = seq_dir / "imu.csv"
    if not src.is_file():
        raise SystemExit(f"missing {src}")

    out_dir = seq_dir / "mav0" / "imu0"
    out_dir.mkdir(parents=True, exist_ok=True)
    dst = out_dir / "data.csv"

    n = 0
    with src.open() as fin, dst.open("w", newline="") as fout:
        rd = csv.reader(fin)
        first = next(rd)
        # Skip header if present
        try:
            float(first[0])
            rows = [first]
        except ValueError:
            rows = []
        rows.extend(rd)
        w = csv.writer(fout)
        fout.write(HEADER + "\n")
        for row in rows:
            t_s = float(row[0])
            ax, ay, az = float(row[1]), float(row[2]), float(row[3])
            gx, gy, gz = float(row[4]), float(row[5]), float(row[6])
            t_ns = int(round(t_s * 1e9))
            w.writerow([t_ns, gx, gy, gz, ax, ay, az])
            n += 1

    # Minimal sensor.yaml - users should refine the noise model per algorithm.
    (out_dir / "sensor.yaml").write_text(
        "# Auto-generated by scripts/data/imu_to_euroc.py\n"
        "# Refine the IMU noise parameters per algorithm if needed.\n"
        "sensor_type: imu\n"
        f"comment: converted from {src.name}\n"
        f"rate_hz: {rate_hz}\n"
        "gyroscope_noise_density:       1.6968e-04\n"
        "gyroscope_random_walk:         1.9393e-05\n"
        "accelerometer_noise_density:   2.0000e-03\n"
        "accelerometer_random_walk:     3.0000e-03\n"
        "# T_BS: 4x4 sensor-to-body extrinsic; left-cam frame assumed if omitted.\n"
    )
    print(f"[ok] {dst}  ({n} samples, header EuRoC-ASL)")
    print(f"[ok] {out_dir/'sensor.yaml'}  (placeholder noise model)")
    return n


def main():
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("seq_dir", type=Path)
    ap.add_argument("--rate", type=int, default=200,
                    help="Nominal IMU rate (Hz) recorded in sensor.yaml")
    args = ap.parse_args()
    convert(args.seq_dir, args.rate)


if __name__ == "__main__":
    main()
